from osgeo import gdal
from matplotlib import pyplot as plt
import numpy as np


##################################################################################################################
############################################## INPUTS ############################################################
##################################################################################################################


Raster = '/Users/christianlannefranque/Desktop/NDSI2reprojected.tif'	# image to be analyzed


##################################################################################################################
############################################## PROCEDURE #########################################################
##################################################################################################################


# Openin of the file
ds = gdal.Open(Raster,gdal.GA_ReadOnly)	
arr = ds.ReadAsArray()


# Histogram implementation
plt.hist(arr.ravel(),2000,[-1,1])	# 2000 bins for the histogram / interval [-1,1] for the NDSI


# Display
plt.show()
